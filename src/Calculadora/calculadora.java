package Calculadora;

public class calculadora {

    public void adicion(int primerNumero, int segundoNumero, int tercerNumero)
    {

        System.out.println("El resultado de sumar " + primerNumero + " + " + segundoNumero + " es igual a " + tercerNumero);

    }

    public void resta(int primerNumero, int segundoNumero, int tercerNumero)
    {

        System.out.println("El resultado de restar " + primerNumero + " - " + segundoNumero + " es igual a " + tercerNumero);

    }

    public void multiplicacion(int primerNumero, int segundoNumero, int tercerNumero)
    {

        System.out.println("El resultado de multiplicar " + primerNumero + " * " + segundoNumero + " es igual a " + tercerNumero);

    }

    public void division (int primerNumero, int segundoNumero, int tercerNumero)
    {

        System.out.println("El resultado de dividir " + primerNumero + " / " + segundoNumero + " es igual a " + tercerNumero);

    }


}
